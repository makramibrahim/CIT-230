<h3>Syrian Refugees Pay Smugglers To Take Them Back To Turkey</h3>

<video class="featured-video" controls preload="metadata" poster="/images/refugeecrisis.jpg" src="video/smuggle.mp4"></video><br><br>

<p>Now thousands of migrants set out every day to make the long journey from refugee camps neighboring Syria to the Western European nations that have offered them safe harbor. They do so hand-in-hand with smugglers, who often advertise their services openly on Facebook and other social media sites.</p>
<img class="featured-image" src="images/smulgglesrefugees.jpg" alt="smuggle demo"/>

<p><strong>According to Washington Post:</strong>“Asylum-seekers have powerful motivations to make it to wealthy nations such as Germany and Sweden without being detected along the way. If authorities in other E.U. countries detain them, they can be forced to apply for asylum under far less-welcoming circumstances. Hungary has strung up a razor-wire fence along its frontier with Serbia, and Hungarian leaders this week barred their onward journey to Austria. Germany, meanwhile, expects to take up to 800,000 asylum-seekers this year. Critics say that type of patchwork enforcement only fuels the demand for smugglers."</p>


