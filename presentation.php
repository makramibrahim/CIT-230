   <h3>Columns</h3>
   <p>HTML does not provide a tag for table columns.
In the original HTML specifications, the number of columns is determined by how many cells are inserted within each row.
for example, if you have four tags in each table row, that table has four columns
Later versions of HTML provide increased support for controlling the appearance of table columns.
   </p><br>
   
   <h3>Spanning Rows and Columns</h3>
   <p>To merge several cells into one, you need to create a spanning cell.
A spanning cell is a cell that occupies more than one row or column in a table.
Spanning cells are created by inserting the rowspan and colspan attribute in a tag.
The syntax for these attributes is: rowspan=“value” colspan=“value”
value is the number of rows or columns that the cell spans in the table
   </p><br>
   
