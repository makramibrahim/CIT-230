  <h3>WHY VOLUNTEER?</h3>
  
  <img class="featured-image" src="images/refugeevolunteer.jpg" alt="Demo image"/>

  <div id="main">
   <p>
    Refugee volunteers are in high demand, due to the astronomically high number of refugees spread across the world. Refugee camps exist in almost every country in the world, though their numbers fluctuate depending on the current political climate.<br><br>
 
    Refugees are often forgotten people in need, those turned away from their homes and sometimes their own countries, because of this refugee relief organizations are often understaffed, hence there is a need for refugee relief volunteers.<br><br>
    Help refugee online rises in demand when a new conflict arises and people are in need of immediate aid. Refugee relief volunteers truly make a difference in these time sensitive situations. Volunteers that work with refugee charities abroad are in high-demand as well, as acts such as fundraising and other organizational tasks are hugely helpful when the need for financial support is needed quickly.<br><br>
    If you’re earning a degree in counseling, social work, or sociology, help refugee online will be a priceless experience. You will get a chance to interact with refugee families, help them process how their lives have quickly changed, and help them move forward into the future, however uncertain it may be. This hands on experience with victims of conflict is an altruistic journey unlike any other. </p>
   
   <h3>BENEFITS</h3>
   <p>There are obvious benefits surrounding refugee volunteer work abroad, including practical skills to put on your resume. Volunteering in these camps all over the world can show to potential employers that you are willing to travel for projects you are passionate about, that you have the multicultural skills to interact with refugees from another country as well as teach them relevant skills. As always, volunteering with help refugee online also illustrates your philanthropic spirit.<br><br>

    The biggest benefits of help refugee online is the experiences you can’t define on your resume. Refugees are people with heartbreaking experiences, so it requires a high level of emotional maturity to put yourself in a volunteer situation where refugee families can share their stories with you. Sensitivity is required when dealing with these circumstances, and especially when working with children who have been forced to flee from their homes. Worldwide refugees are comprised of 80 percent women and children.<br><br>

Volunteering with refugee certainly don't come without its challenges, but it is these challenges that will help you move outside of your comfort zone and grow as an adult, a volunteer, and an ally.</p>
   
   
