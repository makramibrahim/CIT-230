   <p><strong>Our Vision:</strong> 
    Every person who participates in HRO program or project will have a better chance to take control of their lives and achieve self sufficiency.<br><br>

    <strong>Our Principles</strong><br>
We strive to operate by the following guiding principles: 

<strong>Ethics:</strong> We hold ourselves to the highest standards of transparency, accountability and ethical behavior.<br><br>
<strong>Dignity:</strong> We preserve and enhance the dignity of the people we serve.<br><br>
<strong>Optimism:</strong> We provide hope for those in need to have a better life.<br><br>
<strong>Service:</strong> We use a participatory approach to enhance the delivery of our services that is appropriate and relevant to those we serve.<br><br>
<strong>Self-sufficiency:</strong> We enhance the self-sufficiency of the people we serve and empower them to rebuild productive lives.<br><br>
<strong>Advocacy:</strong> We advocate for the human needs of the people we serve and inspire the world community to respond to their needs.<br><br>
<strong>Stewardship:</strong> We are stewards of the human and financial resources we have been given to ensure they serve those with great need.
