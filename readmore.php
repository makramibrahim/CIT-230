  <h3>Refugees in Greece's Chios fear violence from far-right</h3>
  <p>Up to 8,400 refugees and migrants are stuck on Greek islands during the slow asylum process.</p>
  <img class="featured-image" src="images/seekforasylum.jpg" alt="seek asylum demo"/>
   
  <p>Chios island, Greece - The graves of an Afghan child and a Syrian man are tucked away in the back corner of a local cemetery. Large mounds of red dirt sit next to six empty holes in the earth.<br>

   The ground's open wounds lie in wait for those who have fallen victim to the refugee crisis gripping Europe, as people continue to flee war and poverty with the hopes of finding security. <br><br>

   "Our beloved Ali Reza, we love you and we carry you in our hearts," reads a handwritten letter propped up against a bouquet of flowers on the grave of the young Afghan boy, who died after falling out of the dinghy and hitting his head on the island's rocky shore. <br><br>

   A little white teddy bear lies face down on the dirt next to a couple toys.<br><br>

   Located only 15 minutes north of Chios town, the cemetery is a haunting reminder of the risks displaced people take in order to escape war and hunger. </p><br>
  
  
  <img class="featured-image" src="images/gravesrefugees.jpg" alt="graves demo"/>
  <p>Ahmad Hamdan, a 46-year-old Syrian lawyer, lives with nine of his relatives in Dipethe, an impromptu refugee camp in a deserted theatre five minutes down the road from the Chios port.<br><br>

Sitting under a makeshift home of tarpaulins on a sweltering June afternoon, he explains that they fled the bloodshed in Syria only to find themselves fearing right-wing locals and internal violence in the island's camps. <br><br>

 When violent clashes broke out between Afghans and Syrians in the Vial refugee camp in April, Ahmad's family joined hundreds of people - mostly Syrians and Iraqis - who made the seven kilometre walk to the town's port.<br><br>

The family slept in the port for four days until locals attacked them with bottles and fireworks.
  </p>
  
  <h3>'We are stuck here'</h3>
  <p>Police subsequently moved them to the Dipethe camp, where some 300 people live in tents in and around the theatre. <br><br>

   Because they arrived on March 20, the day when an agreement between the European Union and Turkey to control the flow of refugees and migrants, Ahmad and his relatives are barred from leaving the island without the Greek government's permission.<br>

   Their only options are to request asylum in Greece or return to Turkey.<br>

Deportations have taken place at a snail's pace, however, since the deal was struck, and the vast majority of refugees and migrants on the Greek islands are stuck in limbo as they wait to find out their fates.</p>
  
  <h3>Attacks</h3>
  <img class="featured-image" src="images/ahmed.jpg" alt="ahmed demo"/>
  <p>Ahmad and Muhammad first met Antonis Vorrias, a 49-year-old local who volunteers with refugees, when he helped them disembark from their dinghy boat upon arriving in Chios. <br><br>

   The small but vocal group of right-wing activists in Chios haven't spared the volunteers, aid workers and government employees who work in the camps.<br><br>

   Sitting at a cafe in the town centre, Antonis and Yannis Koutsodonotis, a 50-year-old Greek Ministry of Health employee, explain that the situation has grown steadily more tense since the EU-Turkey deal was struck in March.<br><br>

   On the afternoon of June 6, Yannis went to the local Souda refugee camp after a fire broke out during clashes between Syrians and Afghans.<br><br>

   "I was there to coordinate the ambulance and to let the hospital know if there were many injured people," he tells Al Jazeera.<br><br>

   "There was a guy, a fascist, who was shouting [at the refugees]," he remembers, saying that the man suddenly punched a 15-year-old unaccompanied Syrian boy in the head.<br><br>
